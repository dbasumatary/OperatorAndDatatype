public class Datatype {
    public static void main(String[] args) {
        //Boolean datatype
        boolean flag = true;
        System.out.println(flag);     //print true

        //Byte datatype
        byte range;
        range=123;
        System.out.println(range);  //print 123

        //Short datatype
        short temp;
        temp=-242;
        System.out.println(temp);   //print -242

        //Int datatype
        int number = -2345;
        System.out.println(number);   //print -2345

        //Long datatype
        long num = -276432L;
        System.out.println(num);    //print -2764321

        //Double datatype
        double value = 7684.238775054;
        System.out.println(value);   //print 7684.238775054

        //Float datatype
        float abc = -37.7652965f;
        System.out.println(abc);   //print -37.7652965f

        //Char datatype
        char letter1 = 'A';
        char letter2 = '1';
        char letter3 = 'T';
        System.out.println(letter1);   //print A
        System.out.println(letter2);
        System.out.println(letter3);
    }
}
