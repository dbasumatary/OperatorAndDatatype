package com.operators;

public class Arithmetic {
    public static void main(String[] args) {
        double a = 12, b = 7, c = 24;
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
        System.out.println(10*40/5+7-3*8/2);
        System.out.println("c++ = " + (c++));
        System.out.println("++c = " + (++c));
    }
}
